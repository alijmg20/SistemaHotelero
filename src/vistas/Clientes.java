    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (!this.txtcodigo.getText().isEmpty() && !this.txtfechanac.getText().isEmpty() && !this.txtnacimiento.getText().isEmpty() && !this.txtnombre.getText().isEmpty() && (!this.rbf.isSelected() || !this.rbm.isSelected())) {
            int codigo = Integer.parseInt(this.txtcodigo.getText());
            String nombre = this.txtnombre.getText();
            String fechaString = this.txtfechanac.getText();
            long fechanac = Date.parse(fechaString);
            Date fechaDate = new Date(fechanac);
            String lugarn = this.txtnacimiento.getText();
            char sexo = 'M';
            if (this.rbm.isSelected()) {
                sexo = 'M';
            } else if (this.rbf.isSelected()) {
                sexo = 'F';
            }
            String observaciones = " ";
            observaciones = this.txtobservaciones.getText();

            cliente.insertarCliente(codigo, nombre, fechaDate, lugarn, sexo, observaciones);
            this.tableclientes.setModel(cliente.mostrarClientes());
        } else {
            JOptionPane.showMessageDialog(null, "No ha ingresado por completo los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!this.txtcodigo.getText().isEmpty() && !this.txtfechanac.getText().isEmpty() && !this.txtnacimiento.getText().isEmpty() && !this.txtnombre.getText().isEmpty() && (!this.rbf.isSelected() || !this.rbm.isSelected())) {
            int codigo = Integer.parseInt(this.txtcodigo.getText());
            String nombre = this.txtnombre.getText();
            long fechanac = Date.parse(this.txtfechanac.getText());
            Date fechan = new Date(fechanac);
            String lugarn = this.txtnacimiento.getText();
            char sexo = 'M';
            if (this.rbm.isSelected()) {
                sexo = 'M';
            } else if (this.rbf.isSelected()) {
                sexo = 'F';
            }
            String observaciones = " ";
            observaciones = this.txtobservaciones.getText();

            int decision = JOptionPane.showConfirmDialog(null, "¬øDesea actualizar este Cliente ? ");
            if (decision == 0) {
                cliente.actualizarClientes(codigo, nombre, fechan, lugarn, sexo, observaciones);
                this.tableclientes.setModel(cliente.mostrarClientes());
            }

        } else {
            JOptionPane.showMessageDialog(null, "No ha ingresado por completo los datos", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tableclientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableclientesMouseClicked

        int filaSeleccionada = this.tableclientes.rowAtPoint(evt.getPoint());
        this.txtcodigo.setText(this.tableclientes.getValueAt(filaSeleccionada, 0).toString());
        this.txtnombre.setText(this.tableclientes.getValueAt(filaSeleccionada, 1).toString());
        this.txtfechanac.setText(this.tableclientes.getValueAt(filaSeleccionada, 2).toString());
        this.txtnacimiento.setText(this.tableclientes.getValueAt(filaSeleccionada, 3).toString());
        if (this.tableclientes.getValueAt(filaSeleccionada, 4).toString().charAt(0) == 'M') {
            this.rbm.setSelected(true);
        } else if (this.tableclientes.getValueAt(filaSeleccionada, 4).toString().charAt(0) == 'F') {
            this.rbf.setSelected(true);
        }
        this.txtobservaciones.setText(this.tableclientes.getValueAt(filaSeleccionada, 5).toString());


    }//GEN-LAST:event_tableclientesMouseClicked

